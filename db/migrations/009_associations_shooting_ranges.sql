-- 協会×射撃場 中間テーブル
CREATE TABLE IF NOT EXISTS association_shooting_ranges (
  association_cd INTEGER NOT NULL REFERENCES associations(cd) ON DELETE CASCADE,
  shooting_range_id INTEGER NOT NULL REFERENCES shooting_ranges(id) ON DELETE CASCADE,
  PRIMARY KEY (association_cd, shooting_range_id)
);
