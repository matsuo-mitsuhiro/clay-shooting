// 変換用の正規表現パターン（一度だけ生成）
const OLD_KANJI_PATTERN = new RegExp(
  Object.keys(OLD_TO_NEW_KANJI).map(c => c.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')).join('|'),
  'g'
);

/**
 * 旧字体→新字体変換 + NFKC正規化 + 全角英数→半角英数変換
 *
 * @param str 入力文字列
 * @returns 正規化された文字列
 */
export function normalizeKanji(str: string): string {
  if (!str) return str;

  // 1. Unicode NFKC正規化（全角英数→半角英数も含む）
  let result = str.normalize('NFKC');

  // 2. 旧字体→新字体変換
  result = result.replace(OLD_KANJI_PATTERN, (match) => OLD_TO_NEW_KANJI[match] ?? match);

  return result;
}
